CREATE DATABASE ESCOLA
GO
USE ESCOLA
GO
CREATE TABLE PROFESSOR(
COD_PROFESSOR INT PRIMARY KEY, 
NOME VARCHAR(20),
SOBRENOME VARCHAR(30),
TELEFONE CHAR(12)
)
GO
CREATE TABLE AULA(
COD_AULA INT PRIMARY KEY,
COD_PROFESSOR INT, 
SALA VARCHAR(20),
NUMSEQ INT,
NIVEL_DIF VARCHAR(30)
)
GO
CREATE TABLE ALUNO(
COD_ALUNO INT PRIMARY KEY,
NOME VARCHAR(30),
IDADE CHAR(2),
SEXO VARCHAR(15),
TELEFONE CHAR(12),
INSTITUICAO VARCHAR(30)
)
GO
CREATE TABLE AULA_ALUNO(
COD_AULA INT,
COD_ALUNO INT
)
go
alter table AULA
ADD CONSTRAINT FK_PROFESSOR_AULA FOREIGN KEY (COD_PROFESSOR) REFERENCES PROFESSOR (COD_PROFESSOR)
GO
ALTER TABLE AULA_ALUNO 
ADD CONSTRAINT FK_AULA_AULA_ALUNO FOREIGN KEY(COD_AULA) REFERENCES AULA (COD_AULA)
GO
ALTER TABLE AULA_ALUNO
ADD CONSTRAINT FK_ALUNO_AULA_ALUNO FOREIGN KEY (COD_ALUNO) REFERENCES ALUNO (COD_ALUNO)

